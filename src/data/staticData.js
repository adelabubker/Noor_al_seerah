// Helper functions matching backend API behavior
// This helper reproduces simple backend-style filtering by search text, category, and optional result limits.
export function getCompanions({ search = '', category = '', featured = false, limit = null } = {}) {
  let result = [...companions];

  if (search) {
    const q = search.toLowerCase();
    result = result.filter(c =>
      c.nameAr.includes(search) ||
      c.name.toLowerCase().includes(q) ||
      (c.description || c.shortBio || '').includes(search)
    );
  }

  if (category) {
    result = result.filter(c => c.category === category);
  }

  if (limit) {
    result = result.slice(0, limit);
  }

  return result;
}

// Finds a single companion record based on the URL slug shown in the details route.
export function getCompanionBySlug(slug) {
  return companions.find(c => c.slug === slug) || null;
}

// Returns timeline events sorted by display order, with optional filtering by phase.
export function getSeerahEvents({ phase = '' } = {}) {
  let result = [...seerahEvents].sort((a, b) => a.order - b.order);
  if (phase) result = result.filter(e => e.phase === phase);
  return result;
}
